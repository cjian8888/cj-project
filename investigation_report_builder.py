# ==================== 加载器函数 ====================


def load_investigation_report_builder(
    output_dir: str = "./output",
) -> Optional[InvestigationReportBuilder]:
    """
    加载初查报告构建器

    【修复】优先加载完整的 analysis_results_cache.json，包含:
    - profiles（画像）
    - precisePropertyData（房产数据，按身份证号索引）
    - vehicleData（车辆数据）
    - familySummary（家庭关系）
    - 其他外部数据

    如果完整缓存不存在，回退到分散的缓存文件。

    Args:
        output_dir: 输出目录路径

    Returns:
        InvestigationReportBuilder 实例，如果缓存不存在则返回 None
    """
    # 【修复】优先尝试加载完整缓存文件
    full_cache_path = os.path.join(output_dir, "analysis_results_cache.json")
    if os.path.exists(full_cache_path):
        try:
            with open(full_cache_path, "r", encoding="utf-8") as f:
                analysis_cache = json.load(f)
            logger.info(f"[初查报告] 已加载完整缓存: analysis_results_cache.json")
            logger.info(
                f"[初查报告] 缓存包含: {', '.join(list(analysis_cache.keys())[:10])}"
            )
            return InvestigationReportBuilder(analysis_cache, output_dir)
        except Exception as e:
            logger.warning(f"[初查报告] 加载完整缓存失败: {e}，回退到分散文件")

    # 回退：从分散的缓存文件加载
    cache_dir = os.path.join(output_dir, "analysis_cache")

    if not os.path.exists(cache_dir):
        logger.warning(f"[初查报告] 缓存目录不存在: {cache_dir}")
        return None

    analysis_cache = {}

    cache_files = {
        "profiles": "profiles.json",
        "derived_data": "derived_data.json",
        "suspicions": "suspicions.json",
        "graph_data": "graph_data.json",
        "metadata": "metadata.json",
    }

    for key, filename in cache_files.items():
        filepath = os.path.join(cache_dir, filename)

        # profiles 特殊处理，优先加载完整版
        if key == "profiles":
            full_filepath = os.path.join(cache_dir, "profiles_full.json")
            if os.path.exists(full_filepath):
                try:
                    with open(full_filepath, "r", encoding="utf-8") as f:
                        analysis_cache[key] = json.load(f)
                    logger.info(f"[初查报告] 已加载完整画像: profiles_full.json")
                    continue
                except Exception as e:
                    logger.warning(
                        f"[初查报告] 加载 profiles_full.json 失败: {e}，回退到简化版"
                    )

        if os.path.exists(filepath):
            try:
                with open(filepath, "r", encoding="utf-8") as f:
                    analysis_cache[key] = json.load(f)
                logger.info(f"[初查报告] 已加载: {filename}")
            except Exception as e:
                logger.warning(f"[初查报告] 加载 {filename} 失败: {e}")
                analysis_cache[key] = {}
        else:
            logger.warning(f"[初查报告] 文件不存在: {filepath}")
            analysis_cache[key] = {}

    return InvestigationReportBuilder(analysis_cache, output_dir)


