    def _is_numeric(self, value) -> bool:
        """判断值是否可以转换为数字"""
        try:
            float(value)
            return True
        except (ValueError, TypeError):
            return False

    def _get_person_id(self, name: str) -> Optional[str]:
        """根据姓名获取身份证号"""
        return self._name_to_id_map.get(name)

    def _get_person_name(self, person_id: str) -> Optional[str]:
        """根据身份证号获取姓名"""
        # 标准化身份证号（去除科学计数法等格式问题）
        try:
            normalized_id = str(int(float(person_id)))
        except (ValueError, TypeError):
            normalized_id = str(person_id)
        return self._id_to_name_map.get(normalized_id)

    def _clean_nan(self, value, default: str = "未知") -> str:
        """
        【审计修复】清理nan/None值，返回可读字符串

        Args:
            value: 要清理的值
            default: 默认替换值

        Returns:
            清理后的字符串
        """
        if value is None:
            return default
        str_val = str(value).strip()
        if str_val.lower() in ("nan", "none", "null", ""):
            return default
        return str_val

    def _clean_transaction(self, trans: Dict) -> Dict:
        """清理交易记录中的nan值"""
        cleaned = {}
        for key, value in trans.items():
            if key == "counterparty":
                cleaned[key] = self._clean_nan(value, "无对手方信息")
            elif key == "description":
                cleaned[key] = self._clean_nan(value, "无交易描述")
            elif isinstance(value, (int, float)):
                cleaned[key] = value
            else:
                cleaned[key] = self._clean_nan(value, "")
        return cleaned

    # ==================== 铁律-数据回退工具 ====================

    def _load_workbook_sheet(self, sheet_name: str) -> Optional[List[Dict]]:
        """
        【铁律-数据回退】从核查底稿Excel读取工作表数据

        当JSON缓存中缺失某类数据时，尝试从核查底稿Excel读取。
        使用缓存避免重复读取同一工作表。

        Args:
            sheet_name: 工作表名称

        Returns:
            数据列表，如果工作表不存在则返回None
        """
        # 检查缓存
        if sheet_name in self._workbook_cache:
            return self._workbook_cache[sheet_name]

        workbook_path = os.path.join(
            self.output_dir, "analysis_results", "资金核查底稿.xlsx"
        )
        if not os.path.exists(workbook_path):
            logger.debug(f"[核查底稿] 文件不存在: {workbook_path}")
            self._workbook_cache[sheet_name] = None
            return None

        try:
            import pandas as pd

            df = pd.read_excel(workbook_path, sheet_name=sheet_name)
            data = df.to_dict("records")
            self._workbook_cache[sheet_name] = data
            logger.info(f"[核查底稿] 从工作表'{sheet_name}'读取 {len(data)} 条记录")
            return data
        except ValueError as e:
            # 工作表不存在
            logger.debug(f"[核查底稿] 工作表'{sheet_name}'不存在: {e}")
            self._workbook_cache[sheet_name] = None
            return None
        except Exception as e:
            logger.warning(f"[核查底稿] 读取工作表'{sheet_name}'失败: {e}")
            self._workbook_cache[sheet_name] = None
            return None

    def _get_workbook_sheet_names(self) -> List[str]:
        """
        获取核查底稿Excel的所有工作表名称

        Returns:
            工作表名称列表
        """
        workbook_path = os.path.join(
            self.output_dir, "analysis_results", "资金核查底稿.xlsx"
        )
        if not os.path.exists(workbook_path):
            return []

        try:
            import pandas as pd

            xl = pd.ExcelFile(workbook_path)
            return xl.sheet_names
        except Exception as e:
            logger.warning(f"[核查底稿] 读取工作表列表失败: {e}")
            return []

