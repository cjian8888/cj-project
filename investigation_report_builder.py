    # ==================== v3.0 新增：时空碰撞分析 ====================
    
    def _build_timeline_collision_module(self, primary_person: str, 
                                          investigation_config: InvestigationConfig = None) -> TimelineCollisionModule:
        """
        构建时空碰撞分析模块
        
        判断资产购置时间与入职/提拔时间的关联性：
        - 购房/购车时间早于入职时间 → 需核实资金来源
        - 购房/购车发生在提拔前后 → 重点关注
        """
        collisions = []
        
        # 获取入职/提拔时间
        entry_date = ""
        promotion_date = ""
        if investigation_config and investigation_config.primary_subject:
            entry_date = investigation_config.primary_subject.entry_date
            promotion_date = investigation_config.primary_subject.promotion_date
        
        # 从 profiles 中获取房产/车辆信息
        profile = self.profiles.get(primary_person, {})
        
        # 房产信息（需外部数据支持）
        properties = profile.get('properties', []) or []
        for prop in properties:
            reg_date = prop.get('registration_date', '') or prop.get('登记时间', '')
            if not reg_date:
                continue
                
            collision = self._check_timeline_collision(
                asset_type="房产",
                asset_description=prop.get('description', '') or prop.get('地址', ''),
                registration_date=reg_date,
                entry_date=entry_date,
                promotion_date=promotion_date,
                profile=profile
            )
            if collision:
                collisions.append(collision)
        
        # 车辆信息（需外部数据支持）
        vehicles = profile.get('vehicles', []) or []
        for vehicle in vehicles:
            reg_date = vehicle.get('registration_date', '') or vehicle.get('登记时间', '')
            if not reg_date:
                continue
                
            collision = self._check_timeline_collision(
                asset_type="车辆",
                asset_description=vehicle.get('description', '') or vehicle.get('车型', ''),
                registration_date=reg_date,
                entry_date=entry_date,
                promotion_date=promotion_date,
                profile=profile
            )
            if collision:
                collisions.append(collision)
        
        high_risk_count = sum(1 for c in collisions if c.risk_level == 'high')
        
        return TimelineCollisionModule(
            collisions=collisions,
            total_count=len(collisions),
            high_risk_count=high_risk_count
        )
    
    def _check_timeline_collision(self, asset_type: str, asset_description: str,
                                   registration_date: str, entry_date: str,
                                   promotion_date: str, profile: Dict) -> Optional[TimelineCollision]:
        """检查单个资产的时空碰撞"""
        try:
            from datetime import datetime
            
            reg_dt = datetime.strptime(registration_date[:10], '%Y-%m-%d')
            
            related_event = ""
            event_date = ""
            time_gap_months = 0
            risk_level = "low"
            verdict = ""
            
            # 检查与入职时间的关系
            if entry_date:
                entry_dt = datetime.strptime(entry_date[:10], '%Y-%m-%d')
                gap = (reg_dt - entry_dt).days / 30  # 月数
                time_gap_months = int(gap)
                
                if gap < 0:
                    # 资产购置早于入职
                    related_event = "入职"
                    event_date = entry_date
                    risk_level = "high" if abs(gap) < 12 else "medium"
                    verdict = f"资产购置时间早于入职{abs(time_gap_months)}个月，需核实资金来源"
            
            # 检查与提拔时间的关系（优先级更高）
            if promotion_date:
                promo_dt = datetime.strptime(promotion_date[:10], '%Y-%m-%d')
                gap = (reg_dt - promo_dt).days / 30
                
                if abs(gap) < 12:  # 提拔前后12个月内
                    related_event = "提拔"
                    event_date = promotion_date
                    time_gap_months = int(gap)
                    risk_level = "high"
                    if gap < 0:
                        verdict = f"资产购置发生在提拔前{abs(time_gap_months)}个月，需重点核实"
                    else:
                        verdict = f"资产购置发生在提拔后{time_gap_months}个月，需重点核实"
            
            if not related_event:
                return None
            
            # 检查本人流水是否有支付记录
            # TODO: 从 derived_data 中查找大额支出
            fund_source_found = False
            fund_source_amount = 0.0
            
            return TimelineCollision(
                asset_type=asset_type,
                asset_description=asset_description,
                registration_date=registration_date,
                related_event=related_event,
                event_date=event_date,
                time_gap_months=time_gap_months,
                fund_source_found=fund_source_found,
                fund_source_amount=fund_source_amount,
                risk_level=risk_level,
                verdict=verdict
            )
        except Exception as e:
            logger.warning(f"[时空碰撞] 日期解析失败: {e}")
            return None
    
    # ==================== v3.0 新增：负债压力分析 ====================
    
    def _build_debt_stress_analysis(self, name: str) -> DebtStressAnalysis:
        """
        构建负债压力分析
        
        关注：
        - 网贷平台数量（≥3个为多头借贷）
        - 月均还款压力
        - 借贷总额
        """
        profile = self.profiles.get(name, {})
        loan_data = self.derived_data.get('loan', {})
        
        # 从 loan 数据中提取网贷信息
        loan_summary = loan_data.get('summary', {}) if isinstance(loan_data, dict) else {}
        loan_details = loan_data.get('details', []) if isinstance(loan_data, dict) else []
        
        # 筛选该人员的网贷记录
        person_loans = [d for d in loan_details if d.get('person') == name]
        
        # 统计网贷平台
        loan_platforms = set()
        total_loan_amount = 0.0
        repayment_total = 0.0
        
        # 网贷平台关键词
        LOAN_PLATFORM_KEYWORDS = getattr(config, 'ONLINE_LOAN_KEYWORDS', [
            '借呗', '花呗', '白条', '微粒贷', '有钱花', '分期乐', '马上消费',
            '360借条', '拍拍贷', '宜人贷', '人人贷', '信用贷', '网贷', '小额贷款'
        ])
        
        for loan in person_loans:
            counterparty = loan.get('counterparty', '')
            for kw in LOAN_PLATFORM_KEYWORDS:
                if kw in counterparty:
                    loan_platforms.add(counterparty)
                    total_loan_amount += loan.get('total_amount', 0) or 0
                    break
        
        # 计算多头借贷风险
        has_multi_head = len(loan_platforms) >= 3
        
        # 风险等级判定
        if has_multi_head or total_loan_amount > 500000:
            risk_level = "high"
            verdict = f"存在多头借贷（{len(loan_platforms)}个平台），借贷总额{total_loan_amount/10000:.1f}万元，财务压力较大"
        elif len(loan_platforms) >= 2 or total_loan_amount > 200000:
            risk_level = "medium"
            verdict = f"存在{len(loan_platforms)}个网贷平台借款，需关注负债情况"
        else:
            risk_level = "low"
            verdict = "未发现明显网贷压力"
        
        return DebtStressAnalysis(
            total_loan_platforms=len(loan_platforms),
            loan_platform_names=list(loan_platforms),
            total_loan_count=len(person_loans),
            total_loan_amount=total_loan_amount,
            has_multi_head_lending=has_multi_head,
            repayment_pressure=repayment_total,
            risk_level=risk_level,
            verdict=verdict
        )
    
