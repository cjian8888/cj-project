    def _build_family_overview_section(
        self, members: List[str], anchor: str
    ) -> FamilyOverviewSection:
        """
        【v4.0】构建家庭整体分析章节

        包含：
        - 家庭资产总览（房产、理财、存款、车辆）
        - 家庭收支汇总
        - 家庭年度工资汇总
        - 综合话术

        Args:
            members: 家庭成员列表
            anchor: 核查对象

        Returns:
            FamilyOverviewSection 对象
        """
        family_name = f"{anchor}家庭"

        # 1. 计算家庭资产总和
        assets_overview = self._calculate_family_assets_total(members)

        # 2. 计算家庭收支汇总
        family_summary = self.derived_data.get("family_summary", {})
        income_expense = family_summary.get("total_income_expense", {})

        total_income = income_expense.get("total_income", 0) or 0
        total_expense = income_expense.get("total_expense", 0) or 0

        # 3. 计算家庭总工资
        total_salary = 0.0
        yearly_salaries = self._aggregate_family_yearly_salary(members)
        for ys in yearly_salaries:
            total_salary += ys.total

        # 计算年均工资
        years_count = len(yearly_salaries) if yearly_salaries else 1
        avg_yearly_salary = total_salary / years_count if years_count > 0 else 0

        # 工资占收入比例
        salary_ratio = (total_salary / total_income * 100) if total_income > 0 else 0

        # 4. 构建成员关系列表
        member_relations = []
        for name in members:
            relation = self._infer_relation_from_config(name, anchor)
            member_relations.append(
                {"name": name, "relation": relation, "has_data": name in self.profiles}
            )

        # 5. 生成综述话术
        narrative = self._generate_family_overview_narrative(
            family_name,
            len(members),
            total_income,
            total_expense,
            total_salary,
            assets_overview,
        )

        assets_narrative = self._generate_family_assets_narrative(assets_overview)
        salary_narrative = self._generate_family_salary_narrative(
            members, total_salary, avg_yearly_salary, yearly_salaries
        )

        return FamilyOverviewSection(
            family_name=family_name,
            anchor=anchor,
            member_count=len(members),
            member_relations=member_relations,
            assets_overview=assets_overview,
            total_income=total_income,
            total_income_wan=round(total_income / 10000, 2),
            total_expense=total_expense,
            total_expense_wan=round(total_expense / 10000, 2),
            total_salary=total_salary,
            total_salary_wan=round(total_salary / 10000, 2),
            avg_yearly_salary=avg_yearly_salary,
            avg_yearly_salary_wan=round(avg_yearly_salary / 10000, 2),
            salary_ratio=round(salary_ratio, 1),
            yearly_salary_breakdown=yearly_salaries,
            narrative=narrative,
            assets_narrative=assets_narrative,
            salary_narrative=salary_narrative,
        )

    def _calculate_family_assets_total(
        self, members: List[str]
    ) -> FamilyAssetsOverview:
        """
        【v4.0】计算家庭资产总和

        汇总所有成员的：
        - 房产数量和价值
        - 银行存款余额
        - 理财持仓金额
        - 车辆数量

        Args:
            members: 家庭成员列表

        Returns:
            FamilyAssetsOverview 对象
        """
        property_count = 0
        property_value = 0.0
        bank_balance = 0.0
        wealth_holding = 0.0
        vehicle_count = 0

        for name in members:
            profile = self.profiles.get(name, {})
            if not profile:
                continue

            # 房产
            properties = profile.get("properties", []) or []
            property_count += len(properties)
            for prop in properties:
                # 尝试获取估值（如果有）
                value = prop.get("estimated_value", 0) or prop.get("value", 0) or 0
                property_value += value

            # 银行存款（使用银行账户余额总和）
            bank_accounts = (
                profile.get("bankAccounts", [])
                or profile.get("bank_accounts", [])
                or []
            )
            for acc in bank_accounts:
                balance = (
                    acc.get("余额", 0)
                    or acc.get("balance", 0)
                    or acc.get("账户余额", 0)
                    or 0
                )
                if isinstance(balance, (int, float)) and balance > 0:
                    bank_balance += balance

            # 理财持仓
            wealth_total = (
                profile.get("wealthTotal", 0) or profile.get("wealth_total", 0) or 0
            )
            wealth_holding += wealth_total

            # 车辆
            vehicles = profile.get("vehicles", []) or []
            vehicle_count += len(vehicles)

        # 总资产
        total_assets = bank_balance + wealth_holding + property_value

        # 生成描述话术
        narrative = self._generate_family_assets_narrative_text(
            property_count,
            property_value,
            bank_balance,
            wealth_holding,
            vehicle_count,
            total_assets,
        )

        return FamilyAssetsOverview(
            property_count=property_count,
            property_value=property_value,
            property_value_wan=round(property_value / 10000, 2),
            bank_balance=bank_balance,
            bank_balance_wan=round(bank_balance / 10000, 2),
            wealth_holding=wealth_holding,
            wealth_holding_wan=round(wealth_holding / 10000, 2),
            vehicle_count=vehicle_count,
            total_assets=total_assets,
            total_assets_wan=round(total_assets / 10000, 2),
            narrative=narrative,
        )

    def _aggregate_family_yearly_salary(
        self, members: List[str]
    ) -> List[FamilyYearlySalary]:
        """
        【v4.0】汇总家庭年度工资

        将所有成员的年度工资按年份聚合。

        Args:
            members: 家庭成员列表

        Returns:
            FamilyYearlySalary 列表
        """
        # 年度工资字典 {year: {member_name: amount, ...}}
        yearly_data = {}

        for name in members:
            profile = self.profiles.get(name, {})
            if not profile:
                continue

            # 尝试从 yearlySalary 获取年度工资数据（驼峰式：yearlySalary.yearly）
            # 修复：profiles.json和analysis_results_cache.json中使用驼峰式字段名
            yearly_salary_obj = profile.get("yearlySalary", {})
            salary_by_year = (
                yearly_salary_obj.get("yearly", {})
                if isinstance(yearly_salary_obj, dict)
                else {}
            )
            salary_by_year = salary_by_year or {}

            for year, year_data in salary_by_year.items():
                if year not in yearly_data:
                    yearly_data[year] = {}
                # 修复：year_data 是字典 {'total': ..., 'transaction_count': ...}
                # 需要提取 total 值
                if isinstance(year_data, dict):
                    yearly_data[year][name] = year_data.get("total", 0) or 0
                else:
                    yearly_data[year][name] = year_data or 0

        # 转换为 FamilyYearlySalary 列表
        result = []
        for year in sorted(yearly_data.keys()):
            member_breakdown = yearly_data[year]
            total = sum(member_breakdown.values())
            result.append(
                FamilyYearlySalary(
                    year=str(year),
                    total=total,
                    total_wan=round(total / 10000, 2),
                    member_breakdown=member_breakdown,
                )
            )

        return result

    def _build_personal_financial_profile(
        self, name: str, profile: Dict
    ) -> PersonalFinancialProfile:
        """
        【v4.0】构建个人资金特征画像

        五维度评分体系：
        - 收支匹配度 (0-25分)
        - 借贷行为 (0-20分)
        - 消费特征 (0-15分)
        - 资金流向 (0-25分)
        - 现金操作 (0-15分)

        Args:
            name: 姓名
            profile: 个人画像数据

        Returns:
            PersonalFinancialProfile 对象
        """
        # 1. 收支匹配度分析
        income_expense_match = self._analyze_income_expense_match(name, profile)

        # 2. 借贷行为分析
        lending_behavior = self._analyze_lending_behavior(name, profile)

        # 3. 消费特征分析
        consumption_pattern = self._analyze_consumption_pattern(name, profile)

        # 4. 资金流向分析
        fund_flow = self._analyze_fund_flow(name, profile)

        # 5. 现金操作分析
        cash_operation = self._analyze_cash_operation(name, profile)

        # 计算总分
        total_score = (
            income_expense_match.score
            + lending_behavior.score
            + consumption_pattern.score
            + fund_flow.score
            + cash_operation.score
        )

        # 判定风险等级
        if total_score <= 30:
            risk_level = "low"
            risk_color = "green"
            risk_label = "低风险"
        elif total_score <= 60:
            risk_level = "medium"
            risk_color = "yellow"
            risk_label = "中风险"
        else:
            risk_level = "high"
            risk_color = "red"
            risk_label = "高风险"

        # 生成专业话术
        professional_narrative = self._generate_professional_narrative(
            name,
            profile,
            total_score,
            risk_level,
            income_expense_match,
            lending_behavior,
            consumption_pattern,
            fund_flow,
            cash_operation,
        )

        conclusion_narrative = self._generate_conclusion_narrative(
            name,
            risk_level,
            total_score,
            [
                income_expense_match,
                lending_behavior,
                consumption_pattern,
                fund_flow,
                cash_operation,
            ],
        )

        # 收集高风险预警
        high_risk_alerts = []
        for dim in [
            income_expense_match,
            lending_behavior,
            consumption_pattern,
            fund_flow,
            cash_operation,
        ]:
            if dim.risk_level == "high":
                for finding in dim.findings:
                    high_risk_alerts.append(finding)

        return PersonalFinancialProfile(
            name=name,
            risk_level=risk_level,
            risk_score=total_score,
            risk_color=risk_color,
            risk_label=risk_label,
            income_expense_match=income_expense_match,
            lending_behavior=lending_behavior,
            consumption_pattern=consumption_pattern,
            fund_flow=fund_flow,
            cash_operation=cash_operation,
            professional_narrative=professional_narrative,
            conclusion_narrative=conclusion_narrative,
            high_risk_alerts=high_risk_alerts,
            has_alerts=len(high_risk_alerts) > 0,
        )

    def _analyze_income_expense_match(
        self, name: str, profile: Dict
    ) -> DimensionAnalysis:
        """分析收支匹配度"""
        total_income = profile.get("totalIncome", 0) or 0
        total_expense = profile.get("totalExpense", 0) or 0
        salary_total = profile.get("salaryTotal", 0) or 0

        # 计算工资占收入比例
        salary_ratio = (salary_total / total_income * 100) if total_income > 0 else 0

        # 计算收支差
        gap = total_expense - total_income
        gap_ratio = (gap / total_income * 100) if total_income > 0 else 0

        findings = []
        score = 0
        risk_level = "low"

        # 评分逻辑
        if gap > 0:
            # 支出超过收入
            if gap_ratio > 50:
                score = 25
                risk_level = "high"
                findings.append(
                    f"支出超过收入{gap / 10000:.1f}万元（超支{gap_ratio:.0f}%），存在重大收支不抵"
                )
            elif gap_ratio > 20:
                score = 15
                risk_level = "medium"
                findings.append(
                    f"支出超过收入{gap / 10000:.1f}万元（超支{gap_ratio:.0f}%），需核实资金来源"
                )
            else:
                score = 8
                findings.append(f"支出略超收入{gap / 10000:.1f}万元")

        # 工资占比过低也是风险
        if salary_ratio < 30 and total_income > 500000:
            score = max(score, 10)
            risk_level = "medium" if risk_level != "high" else "high"
            findings.append(
                f"工资收入仅占总收入{salary_ratio:.0f}%，非工资收入占比较大"
            )

        # 生成话术
        if score == 0:
            narrative = f"{name}收入合计{total_income / 10000:.1f}万元，支出合计{total_expense / 10000:.1f}万元，收支基本平衡，其中工资性收入{salary_total / 10000:.1f}万元，占比{salary_ratio:.0f}%。"
        elif risk_level == "high":
            narrative = f"{name}累计支出{total_expense / 10000:.1f}万元，远超其收入{total_income / 10000:.1f}万元（工资仅{salary_total / 10000:.1f}万元，占比{salary_ratio:.0f}%），需进一步核查超额支出资金来源。"
        else:
            narrative = f"{name}收支匹配度一般，存在{gap / 10000:.1f}万元的支出缺口，建议结合其他证据综合判断。"

        return DimensionAnalysis(
            dimension_name="收支匹配度",
            dimension_icon="💳",
            score=score,
            max_score=25,
            risk_level=risk_level,
            findings=findings,
            narrative=narrative,
        )

    def _analyze_lending_behavior(self, name: str, profile: Dict) -> DimensionAnalysis:
        """分析借贷行为"""
        loan_data = self.derived_data.get("loan", {})
        loan_details = loan_data.get("details", [])

        # 查找该人员的借贷记录
        person_loans = [d for d in loan_details if d.get("person") == name]

        # 网贷平台记录
        online_platforms = self.derived_data.get("online_lending_platforms", [])
        person_platforms = [p for p in online_platforms if p.get("person") == name]

        findings = []
        score = 0
        risk_level = "low"

        # 多头借贷判定
        platform_count = len(person_platforms)
        if platform_count >= 3:
            score = 20
            risk_level = "high"
            findings.append(f"与{platform_count}家网贷平台存在资金往来，属于多头借贷")
        elif platform_count >= 1:
            score = 10
            risk_level = "medium"
            findings.append(f"与{platform_count}家网贷平台存在资金往来")

        # 借贷规模判定
        total_borrowed = sum([l.get("income_total", 0) for l in person_loans])
        if total_borrowed > 1000000:
            score = max(score, 15)
            risk_level = "high" if score >= 15 else risk_level
            findings.append(f"借贷规模较大，累计{total_borrowed / 10000:.1f}万元")

        # 生成话术
        if score == 0:
            narrative = f"经查，{name}名下银行流水未发现与网贷平台的资金往来记录，不存在多头借贷、以贷养贷等异常借贷行为。"
        else:
            narrative = f"经查，{name}与{platform_count}家网贷平台存在资金往来，累计借款{total_borrowed / 10000:.1f}万元，存在一定财务压力。"

        return DimensionAnalysis(
            dimension_name="借贷行为",
            dimension_icon="💴",
            score=score,
            max_score=20,
            risk_level=risk_level,
            findings=findings,
            narrative=narrative,
        )

    def _analyze_consumption_pattern(
        self, name: str, profile: Dict
    ) -> DimensionAnalysis:
        """分析消费特征"""
        # 获取大额支出数据
        large_expenses = self.derived_data.get("large_expenses", {})
        person_expenses = large_expenses.get(name, [])

        findings = []
        score = 0
        risk_level = "low"

