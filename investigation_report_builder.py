    def build_complete_report(
        self,
        primary_person: str,
        doc_number: str = None,
        case_background: str = None,
        data_scope: str = None,
        include_companies: List[str] = None,
    ) -> Dict:
        """
        生成完整初查报告（兼容旧接口）

        Args:
            primary_person: 核查对象（户主）
            doc_number: 文号
            case_background: 案件背景
            data_scope: 数据范围
            include_companies: 需要包含的公司列表（可选）

        Returns:
            完整报告字典
        """
        logger.info(f"[初查报告] 开始生成报告，核查对象: {primary_person}")

        # 1. 构建元信息
        meta = self._build_meta(doc_number, case_background, data_scope)

        # 2. 构建家庭部分
        family = self._build_family_section(primary_person)

        # 3. 构建成员详情
        member_details = self._build_all_member_details(primary_person, family)

        # 4. 构建公司报告
        companies_to_include = include_companies or self._companies
        company_reports = [self._build_company_report(c) for c in companies_to_include]

        # 5. 生成综合研判
        conclusion = self._build_conclusion(member_details, company_reports)

        # 组装完整报告
        report = InvestigationReport(
            meta=meta,
            family=family,
            member_details=member_details,
            company_reports=company_reports,
            conclusion=conclusion,
        )

        logger.info(
            f"[初查报告] 报告生成完成，包含 {len(member_details)} 个成员，{len(company_reports)} 个公司"
        )

        return report.to_dict()

    # ==================== G-05: 按归集配置生成报告 ====================

    def build_report_with_config(
        self,
        config: PrimaryTargetsConfig,
        case_background: str = None,
        data_scope: str = None,
    ) -> Dict:
        """
        【G-05】按归集配置生成报告（新接口）

        根据 primary_targets.json 中的分析单元配置组织报告章节：
        - 核心家庭单元（family）: 聚合成员数据，生成合并章节
        - 独立关联单元（independent）: 每个成员独立成章

        Args:
            config: 归集配置对象
            case_background: 案件背景
            data_scope: 数据范围

        Returns:
            完整报告字典，包含按分析单元组织的章节
        """
        logger.info(
            f"[初查报告] 开始按归集配置生成报告，{len(config.analysis_units)} 个分析单元"
        )

        # 1. 构建元信息
        meta = self._build_meta(config.doc_number, case_background, data_scope)

        # 2. 按分析单元组织章节
        unit_sections = []  # 分析单元章节
        all_member_details = []  # 保留兼容性的成员详情列表

        for unit in config.analysis_units:
            unit_section = self._build_analysis_unit_section(unit)
            unit_sections.append(unit_section)

            # 将成员详情添加到旧格式兼容列表
            for member in unit_section.get("member_details", []):
                all_member_details.append(member)

        # 3. 构建公司报告
        companies_to_include = config.include_companies or self._companies
        company_reports = [self._build_company_report(c) for c in companies_to_include]

        # 4. 构建 family 结构（取第一个家庭单元作为主家庭，兼容旧前端）
        primary_family_unit = (
            config.get_family_units()[0] if config.get_family_units() else None
        )
        if primary_family_unit:
            family = self._build_family_section_from_unit(primary_family_unit)
        else:
            # 无家庭单元时，使用第一个分析单元的锚点
            first_anchor = (
                config.analysis_units[0].anchor if config.analysis_units else ""
            )
            family = self._build_family_section(first_anchor)

        # 5. 生成综合研判
        # 将 unit_sections 中的 member_details 转换为 MemberDetails 对象
        member_details_objs = self._convert_to_member_details_objs(all_member_details)
        conclusion = self._build_conclusion(member_details_objs, company_reports)

        # 6. v3.1 新增：构建家庭分析章节（对齐用户模板）
        household_section = self._build_household_analysis_section(config)

        # 组装完整报告 - 新增 household_analysis_section 字段
        report_dict = {
            "meta": asdict(meta)
            if hasattr(meta, "__dataclass_fields__")
            else meta.to_dict()
            if hasattr(meta, "to_dict")
            else meta,
            "family": family.to_dict()
            if hasattr(family, "to_dict")
            else asdict(family),
            # v3.1 新增：家庭分析章节
            "household_analysis_section": asdict(household_section),
            # 按分析单元组织的章节
            "analysis_units": unit_sections,
            # 兼容旧格式
            "member_details": [
                self._member_details_to_dict(m) for m in member_details_objs
            ],
            "companies": [
                c.to_dict() if hasattr(c, "to_dict") else asdict(c)
                for c in company_reports
            ],
            "conclusion": asdict(conclusion)
            if hasattr(conclusion, "__dataclass_fields__")
            else conclusion.to_dict()
            if hasattr(conclusion, "to_dict")
            else conclusion,
        }

        logger.info(
            f"[初查报告] 按配置生成完成，{len(unit_sections)} 个分析单元，{len(company_reports)} 个公司"
        )

        return report_dict
    
