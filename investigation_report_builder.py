    def _analyze_fund_flow(self, name: str, profile: Dict) -> DimensionAnalysis:
        """分析资金流向"""
        income_data = self.derived_data.get("income", {})
        unknown_income = income_data.get("unknown_source", {}).get(name, [])

        findings = []
        score = 0
        risk_level = "low"

        # 来源不明收入
        unknown_total = sum([u.get("amount", 0) for u in unknown_income])
        if unknown_total > 500000:
            score = 25
            risk_level = "high"
            findings.append(f"存在来源不明收入{unknown_total / 10000:.1f}万元")
        elif unknown_total > 100000:
            score = 15
            risk_level = "medium"
            findings.append(f"存在来源不明收入{unknown_total / 10000:.1f}万元")
        elif unknown_total > 0:
            score = 5
            findings.append(f"存在少量来源不明收入{unknown_total / 10000:.1f}万元")

        # 可疑对手方交易
        suspicious_counterparty = self._get_suspicious_counterparty_transfers(name)
        if suspicious_counterparty:
            score = max(score, 10)
            risk_level = "medium" if risk_level != "high" else "high"
            findings.append(f"与可疑对手方存在{len(suspicious_counterparty)}笔资金往来")

