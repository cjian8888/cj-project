        for item in person_suspicious:
            result.append(
                {
                    "counterparty": item.get("counterparty", ""),
                    "amount": item.get("income_amount", 0),
                    "date": str(item.get("income_date", "")),
                    "days_since": item.get("days_since", 0),
                    "repay_ratio": item.get("repay_ratio", 0),
                    "risk_level": item.get("risk_level", "medium"),
                    "description": item.get("description", ""),
                    "risk_reason": item.get("risk_reason", ""),
                }
            )

        return sorted(result, key=lambda x: x.get("amount", 0), reverse=True)

