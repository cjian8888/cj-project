    def _build_supplier_collision_module(
        self, investigation_config: InvestigationConfig = None
    ) -> SupplierCollisionModule:
        """
        构建供应商碰撞分析模块

        将配置的碰撞目标与所有人员/公司流水进行交叉比对
        """
        results = []
        total_flow_amount = 0.0

        # 获取碰撞目标
        collision_targets = []
        if investigation_config:
            collision_targets = investigation_config.collision_targets

        if not collision_targets:
            return SupplierCollisionModule(
                results=[],
                hit_count=0,
                total_collision_targets=0,
                total_flow_amount=0.0,
            )

        # 遍历每个碰撞目标
        for target in collision_targets:
            target_name = (
                target.name if hasattr(target, "name") else target.get("name", "")
            )

            # 在所有 profiles 中搜索与该供应商的交易
            hit_result = self._search_supplier_flows(target_name)

            if hit_result.has_flow:
                total_flow_amount += hit_result.total_inflow + hit_result.total_outflow

            results.append(hit_result)

        hit_count = sum(1 for r in results if r.has_flow)

        return SupplierCollisionModule(
            results=results,
            hit_count=hit_count,
            total_collision_targets=len(collision_targets),
            total_flow_amount=total_flow_amount,
        )

    def _search_supplier_flows(self, supplier_name: str) -> SupplierCollisionResult:
        """搜索与指定供应商的资金往来"""
        total_inflow = 0.0
        total_outflow = 0.0
        transaction_count = 0
        transactions = []
        first_date = None
        last_date = None

        # 从 derived_data 的 large_transactions 中搜索
        large_txs = self.derived_data.get("large_transactions", [])
        for tx in large_txs:
            counterparty = tx.get("counterparty", "")
            if supplier_name in counterparty or counterparty in supplier_name:
                amount = tx.get("amount", 0)
                direction = tx.get("direction", "")

                if direction == "income":
                    total_inflow += amount
                else:
                    total_outflow += amount

                transaction_count += 1
                transactions.append(tx)

                tx_date = tx.get("date", "")
                if tx_date:
                    if first_date is None or tx_date < first_date:
                        first_date = tx_date
                    if last_date is None or tx_date > last_date:
                        last_date = tx_date

        # 确定流向
        flow_direction = ""
        if total_inflow > 0 and total_outflow > 0:
            flow_direction = "both"
        elif total_inflow > 0:
            flow_direction = "inflow"
        elif total_outflow > 0:
            flow_direction = "outflow"

        return SupplierCollisionResult(
            supplier_name=supplier_name,
            in_collision_list=True,
            has_flow=transaction_count > 0,
            flow_direction=flow_direction,
            total_inflow=total_inflow,
            total_outflow=total_outflow,
            transaction_count=transaction_count,
            first_transaction_date=first_date or "",
            last_transaction_date=last_date or "",
            risk_level="high" if transaction_count > 0 else "medium",
            transactions=transactions[:20],
        )

    # ==================== v3.0 新增：自动生成建议 ====================

    def _build_auto_generated_actions(
        self,
        member_details: List[MemberDetails],
        company_reports: List[CompanyReport],
        timeline_module: TimelineCollisionModule = None,
        supplier_module: SupplierCollisionModule = None,
    ) -> AutoGeneratedActionsModule:
        """
        根据分析结果自动生成下一步工作建议
        """
        actions = []

        # 1. 基于时空碰撞生成建议
        if timeline_module:
            for collision in timeline_module.collisions:
                if collision.risk_level == "high":
                    actions.append(
                        AutoGeneratedAction(
                            action_type="房款盲区",
                            target_name=collision.asset_description,
                            action_text=f"核实{collision.asset_type}（{collision.asset_description}）的购置资金来源，{collision.verdict}",
                            priority="high",
                            related_amount=0.0,  # 需外部数据
                        )
                    )

        # 2. 基于供应商碰撞生成建议
        if supplier_module:
            for result in supplier_module.results:
                if result.has_flow:
                    total = result.total_inflow + result.total_outflow
                    actions.append(
                        AutoGeneratedAction(
                            action_type="企业疑点",
                            target_name=result.supplier_name,
                            action_text=f"核实与{result.supplier_name}的{result.transaction_count}笔往来交易（共{total / 10000:.1f}万元）",
                            priority="high",
                            related_amount=total,
                        )
                    )

        # 3. 基于个人分析生成建议
        for member in member_details:
            # 来源不明金额大
            unknown = member.analysis.inflow_analysis.unknown_source_amount
            if unknown > 100000:
                actions.append(
                    AutoGeneratedAction(
                        action_type="转账疑点",
                        target_name=member.name,
                        action_text=f"核实{member.name}来源不明收入{unknown / 10000:.1f}万元的具体来源",
                        priority="high" if unknown > 500000 else "medium",
                        related_amount=unknown,
                    )
                )

            # 第三方支付金额大
            third_party = member.analysis.third_party_total
            if third_party > 500000:
                actions.append(
                    AutoGeneratedAction(
                        action_type="电子钱包盲区",
                        target_name=member.name,
                        action_text=f"调取{member.name}的电子钱包（微信/支付宝）明细，第三方支付总额达{third_party / 10000:.1f}万元",
                        priority="medium",
                        related_amount=third_party,
                    )
                )

        # 按优先级排序
        priority_order = {"high": 0, "medium": 1, "low": 2}
        actions.sort(key=lambda x: priority_order.get(x.priority, 2))

        high_priority_count = sum(1 for a in actions if a.priority == "high")

        return AutoGeneratedActionsModule(
            actions=actions,
            total_count=len(actions),
            high_priority_count=high_priority_count,
        )

    # ==================== 综合研判构建 ====================

    def _build_conclusion(
        self, member_details: List[MemberDetails], company_reports: List[CompanyReport]
    ) -> InvestigationConclusion:
        """生成综合研判"""
        issues = self._collect_issues(member_details, company_reports)
        next_steps = self._generate_next_steps(issues)
        summary_text = self._generate_summary_text(issues)

        # 统计风险等级
        high_count = sum(1 for i in issues if i.severity == "high")
        medium_count = sum(1 for i in issues if i.severity == "medium")
        low_count = sum(1 for i in issues if i.severity == "low")

        # 统计确认程度
        confirmed_count = sum(1 for i in issues if i.verification_status == "confirmed")
        highly_suspicious_count = sum(
            1 for i in issues if i.verification_status == "highly_suspicious"
        )
        need_verification_count = sum(
            1 for i in issues if i.verification_status == "need_verification"
        )

        # 计算问题涉及总金额
        total_amount = sum(i.amount for i in issues)

        return InvestigationConclusion(
            summary_text=summary_text,
            issues=issues,
            next_steps=next_steps,
            high_risk_count=high_count,
            medium_risk_count=medium_count,
            low_risk_count=low_count,
            confirmed_count=confirmed_count,
            highly_suspicious_count=highly_suspicious_count,
            need_verification_count=need_verification_count,
            total_amount=total_amount,
        )

    def _collect_issues(
        self, member_details: List[MemberDetails], company_reports: List[CompanyReport]
    ) -> List[IssueItem]:
        """收集所有问题"""
        issues = []

        # 1. 个人收支不抵问题
        for member in member_details:
            ratio = member.analysis.income_gap.ratio
            total_income = member.analysis.income_gap.total_income
            salary_income = member.analysis.income_gap.salary_income
            unknown_amount = total_income - salary_income

            if ratio < 30:
                # 工资占比不足30%，高度可疑
                issues.append(
                    IssueItem(
                        person=member.name,
                        issue_type="收支不抵",
                        description=f"工资收入占比仅{ratio:.1f}%，远低于50%，收入来源不明",
                        severity="high",
                        verification_status="highly_suspicious",
                        amount=unknown_amount,
                    )
                )
            elif ratio < 50:
                # 工资占比50%以下，需核实
                issues.append(
                    IssueItem(
                        person=member.name,
                        issue_type="收支不抵",
                        description=f"工资收入占比{ratio:.1f}%，低于50%，需核实其他收入来源",
                        severity="medium",
                        verification_status="need_verification",
                        amount=unknown_amount,
                    )
                )

        # 2. 大额现金问题
        for member in member_details:
            cash_total = member.analysis.large_cash.total_amount
            if cash_total > 500000:  # 超过50万
                # 大额现金需核实
                v_status = (
                    "highly_suspicious" if cash_total > 1000000 else "need_verification"
                )
                issues.append(
                    IssueItem(
                        person=member.name,
                        issue_type="大额现金",
                        description=f"现金交易总额{cash_total / 10000:.1f}万元，需核实现金来源及去向",
                        severity="high" if cash_total > 1000000 else "medium",
                        verification_status=v_status,
                        amount=cash_total,
                    )
                )

        # 3. 公司与关键人员关联问题
        for company in company_reports:
            if company.key_person_transfers.has_transfers:
                transfers = company.key_person_transfers
                # 公司与关键人员有直接转账，高度可疑
                issues.append(
                    IssueItem(
                        person=company.company_name,
                        issue_type="异常资金往来",
                        description=f"与{transfers.unique_persons}名关键人员存在{transfers.transfer_count}笔直接转账，"
                        f"金额合计{transfers.total_amount / 10000:.1f}万元",
                        severity="high",
                        verification_status="highly_suspicious",
                        amount=transfers.total_amount,
                    )
                )

        # 4. 从 suspicions 获取更多问题
        for suspicion in self.suspicions.get("direct_transfers", []) or []:
            amount = suspicion.get("amount", 0)
            risk_level = suspicion.get("risk_level", "medium")
            # 根据风险等级判断确认程度
            v_status = (
                "confirmed"
                if risk_level == "critical"
                else (
                    "highly_suspicious" if risk_level == "high" else "need_verification"
                )
            )
            issues.append(
                IssueItem(
                    person=f"{suspicion.get('from', '')}→{suspicion.get('to', '')}",
                    issue_type="异常资金往来",
                    description=f"直接转账 {amount / 10000:.1f}万元，摘要：{suspicion.get('description', '')}",
                    severity=risk_level,
                    verification_status=v_status,
                    amount=amount,
                )
            )

        # 5. 借贷异常
        loan_details = self.derived_data.get("loan", {}).get("details", []) or []
        for loan in loan_details:
            if loan.get("risk_level") == "high":
                issues.append(
                    IssueItem(
                        person=loan.get("person", ""),
                        issue_type="借贷异常",
                        description=f"与{loan.get('counterparty', '')}存在异常借贷关系：{loan.get('risk_reason', '')}",
                        severity="high",
                        verification_status="need_verification",
                        amount=loan.get("total_amount", 0),
                    )
                )

        return issues

    def _generate_next_steps(self, issues: List[IssueItem]) -> List[str]:
        """生成下一步工作建议"""
        steps = set()

        STEP_TEMPLATES = {
            "收支不抵": "对相关人员的不明收入来源进行约谈核实",
            "异常资金往来": "调取相关银行凭证原件进行核对",
            "大额现金": "核实大额现金交易的来源及去向",
            "借贷异常": "核实相关借贷关系的真实性",
            "资金来源不明": "对资金来源进行深入调查",
        }

        for issue in issues:
            if issue.issue_type in STEP_TEMPLATES:
                steps.add(STEP_TEMPLATES[issue.issue_type])

        # 添加通用建议
        if issues:
            steps.add("对高风险问题进行重点核实")
            steps.add("形成初步调查意见报告")

        return list(steps)

    def _generate_summary_text(self, issues: List[IssueItem]) -> str:
        """生成研判意见文本"""
        if not issues:
            return "经对相关人员资金流水进行穿透分析，未发现明显异常。"

        high_count = sum(1 for i in issues if i.severity == "high")
        medium_count = sum(1 for i in issues if i.severity == "medium")

        text = f"经对相关人员资金流水进行穿透分析，共发现{len(issues)}项问题"

        if high_count > 0:
            text += f"，其中高风险问题{high_count}项"
        if medium_count > 0:
            text += f"，中风险问题{medium_count}项"

        text += "。建议对上述问题进行进一步核实。"

        return text

