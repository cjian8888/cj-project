    def _build_bank_accounts(self, name: str, profile: Dict) -> List[BankAccountInfo]:
        """构建银行账户列表（剔除非真实账户，整合官方余额数据）"""
        accounts = []
        raw_accounts = (
            profile.get("bankAccounts", []) or profile.get("bank_accounts", []) or []
        )

        # 获取官方账户数据（含余额）用于匹配
        official_accounts = profile.get("bank_accounts_official", []) or []
        # 构建账号到余额的映射
        balance_map = {}
        for oa in official_accounts:
            if isinstance(oa, dict):
                acc_num = oa.get("account_number", "") or oa.get("card_number", "")
                if acc_num:
                    balance_map[acc_num] = (
                        oa.get("balance", 0) or oa.get("available_balance", 0) or 0
                    )

        for acc in raw_accounts:
            if isinstance(acc, dict):
                # 支持中文字段名和英文字段名
                bank_name = (
                    acc.get("银行名称", "")
                    or acc.get("bank_name", "")
                    or acc.get("bankName", "")
                )
                account_number = (
                    acc.get("完整账号", "")
                    or acc.get("账号", "")
                    or acc.get("account_number", "")
                )
                account_type = acc.get("账户类别", "") or acc.get(
                    "account_type", "个人账户"
                )
                card_type = acc.get("账户类型", "") or acc.get("card_type", "借记卡")
                status = acc.get("账户状态", "") or acc.get("status", "正常")
                last_tx_date = acc.get("最后交易时间", "") or acc.get(
                    "last_transaction_date", ""
                )

                # 使用缓存中的 是否真实银行卡 字段（如有），否则自行判断
                is_real = acc.get("是否真实银行卡", None)
                if is_real is None:
                    is_real = self._is_real_bank_account(
                        bank_name, account_number, card_type
                    )

                if is_real:
                    # 【2026-01-22 修复】优先使用 last_balance（流水末尾余额），其次 balance
                    # 注意：只使用真实余额数据，不使用净流入估算（净流入≠余额）
                    balance = (
                        acc.get("last_balance", 0)
                        or acc.get("balance", 0)
                        or acc.get("余额", 0)
                        or 0
                    )
                    if balance == 0 and account_number in balance_map:
                        balance = balance_map[account_number]

                    accounts.append(
                        BankAccountInfo(
                            bank_name=bank_name,
                            account_number=account_number,
                            account_type=account_type,
                            card_type=card_type,
                            status=status,
                            balance=balance,
                            last_transaction_date=str(last_tx_date)[:19]
                            if last_tx_date
                            else "",
                        )
                    )

        # 如果流水账户为空，直接使用官方账户
        if not accounts and official_accounts:
            for oa in official_accounts:
                if isinstance(oa, dict):
                    bank_name = oa.get("bank_name", "") or oa.get("反馈单位", "")
                    account_number = oa.get("account_number", "") or oa.get(
                        "card_number", ""
                    )
                    if self._is_real_bank_account(bank_name, account_number, ""):
                        accounts.append(
                            BankAccountInfo(
                                bank_name=bank_name,
                                account_number=account_number,
                                account_type=oa.get("account_type", "个人账户"),
                                card_type="借记卡",
                                status=oa.get("status", "正常"),
                                balance=oa.get("balance", 0)
                                or oa.get("available_balance", 0)
                                or 0,
                                last_transaction_date=oa.get("last_transaction", "")
                                or "",
                            )
                        )

        return accounts

    def _is_real_bank_account(
        self, bank_name: str, account_number: str, card_type: str = ""
    ) -> bool:
        """判断是否为真实银行账户"""
        # 剔除包含关键词的账户
        combined = f"{bank_name}{account_number}{card_type}"
        for keyword in ACCOUNT_EXCLUDE_KEYWORDS:
            if keyword in combined:
                return False

        # 检查账号长度（真实银行卡通常16-19位）
        digits = "".join(c for c in account_number if c.isdigit())
        if len(digits) < 10 or len(digits) > 25:
            return False

        return True

